# snack bar and cinema management

# Scenario:

# You are building a system to manage a small cinema hall with ticket booking and a snack bar.

# Requirements / Tasks:

# Movie Management

# Store movies in dictionary {movie_title: {seats, price}}.

# Ticket Booking

# Users can book seats (update available seats).

# Prevent overbooking.

# Snack Bar

# Store menu in dictionary {item: price}.

# Users can order multiple snacks → add to total bill.

# Discounts

# Combo discount: If ticket + snacks > $30 → 15% discount.

# Reports

# Most watched movie.

# Most sold snack.

# Total revenue.

# Advanced

# Track which users watched which movies (dictionary of lists).

# Recommend movies: “Users who watched X also watched Y”.